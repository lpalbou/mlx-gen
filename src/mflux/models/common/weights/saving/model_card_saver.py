    @staticmethod
    def _tags(terms: str, bits: int | None, pipeline_tag: str) -> list[str]:
        tags = ["mlx", "mlx-gen", "mflux", "apple-silicon"]
        if bits is not None:
            tags.append(f"{bits}-bit")
        if bits == 4 and ("qwen" in terms or "ernie" in terms):
            tags.extend(["mixed-q4", "mixed-q4-q8"])
        if bits == 8 and "wan" in terms:
            tags.append("mixed-q8-bf16")
        if bits == 4 and "fibo" in terms:
            tags.append("mixed-q4-bf16")
        if bits == 8 and "fibo" in terms:
            tags.append("mixed-q8-bf16")
        if "qwen" in terms:
            tags.extend(["qwen", "qwen-image"])
        if ("edit" in terms or pipeline_tag == "image-to-image") and "seedvr2" not in terms:
            tags.append("image-editing")
        if "flux" in terms:
            tags.append("flux")
        if "flux2" in terms or "flux.2" in terms:
            tags.append("flux2")
        if "z-image" in terms or "zimage" in terms:
            tags.append("z-image")
        if "ernie" in terms:
            tags.extend(["ernie", "ernie-image", "ernie-image-turbo"])
        if "wan" in terms:
            tags.extend(["wan", "wan2.2", "video-generation"])
            if ModelCardSaver._wan_supports_text_to_video(terms):
                tags.append("text-to-video")
            if ModelCardSaver._wan_supports_image_to_video(terms):
                tags.append("image-to-video")
            if "a14b" in terms:
                tags.append("wan-a14b")
        if "fibo" in terms:
            tags.append("fibo")
        if "seedvr2" in terms:
            tags.extend(["seedvr2", "image-upscaling", "super-resolution"])
        return ModelCardSaver._deduplicate(tags)

    @staticmethod
    def _pipeline_tag(terms: str) -> str:
        if "wan" in terms:
            if ModelCardSaver._wan_is_i2v_only(terms):
                return "image-to-video"
            return "text-to-video"
        if "seedvr2" in terms:
            return "image-to-image"
        if any(term in terms for term in ("edit", "kontext", "fill", "depth", "controlnet", "redux")):
            return "image-to-image"
        return "text-to-image"

    @staticmethod
    def _model_terms(model: Any) -> str:
        model_config = getattr(model, "model_config", None)
        values = [
            getattr(model_config, "model_name", ""),
            getattr(model_config, "base_model", ""),
            model.__class__.__name__,
        ]
        values.extend(getattr(model_config, "aliases", []) or [])
        return " ".join(str(value) for value in values if value).lower()

    @staticmethod
    def _wan_model_label(terms: str) -> str:
        if "a14b" in terms:
            return "Wan2.2 A14B"
        if "ti2v" in terms or "5b" in terms:
            return "Wan2.2 TI2V-5B"
        return "Wan2.2"

    @staticmethod
    def _seedvr2_model_label(terms: str) -> str:
        if "7b" in terms:
            return "SeedVR2 7B"
        return "SeedVR2 3B"

    @staticmethod
    def _wan_supports_text_to_video(terms: str) -> bool:
        return not ModelCardSaver._wan_is_i2v_only(terms)

    @staticmethod
    def _wan_supports_image_to_video(terms: str) -> bool:
        return ModelCardSaver._wan_is_ti2v_model(terms) or ModelCardSaver._wan_is_i2v_only(terms)

    @staticmethod
    def _wan_is_i2v_only(terms: str) -> bool:
        if any(token in terms for token in ("i2v-a14b", "i2v_a14b", "a14b-i2v", "a14b_i2v")):
            return True
        return "i2v" in terms and "ti2v" not in terms

    @staticmethod
    def _wan_is_ti2v_model(terms: str) -> bool:
        return any(token in terms for token in ("ti2v-5b", "ti2v_5b", "5b-ti2v", "5b_ti2v"))

    @staticmethod
    def _contributor_line(bits: int | None) -> str:
        action = "Prepared" if bits is None else "Quantized"
        return f"{action} and contributed by {ModelCardSaver.CONTRIBUTOR}."

    @staticmethod
    def _model_name(model: Any) -> str:
        model_config = getattr(model, "model_config", None)
        return str(getattr(model_config, "model_name", "unknown-model"))

    @staticmethod
    def _repo_name(model_name: str) -> str:
        return Path(model_name).name.lower().replace("_", "-")

    @staticmethod
    def _is_huggingface_repo_id(model_name: str) -> bool:
        return "/" in model_name and model_name.count("/") == 1 and not model_name.startswith(("./", "../", "~/"))

    @staticmethod
    def _is_flux2_9b(model_name: str, terms: str) -> bool:
        normalized = f"{model_name} {terms}".lower()
        return "flux.2-klein" in normalized and "9b" in normalized

    @staticmethod
    def _is_fibo_model(model_name: str, terms: str) -> bool:
        normalized = f"{model_name} {terms}".lower()
        return "fibo" in normalized

    @staticmethod
    def _flux_noncommercial_license_link(model_name: str) -> str:
        if ModelCardSaver._is_huggingface_repo_id(model_name):
            return f"https://huggingface.co/{model_name}/blob/main/LICENSE.md"
        return ModelCardSaver.FLUX_NON_COMMERCIAL_LICENSE_LINK

    @staticmethod
    def _fibo_license_link(model_name: str) -> str:
        if ModelCardSaver._is_huggingface_repo_id(model_name):
            return f"https://huggingface.co/{model_name}"
        return "https://huggingface.co/briaai/FIBO"

    @staticmethod
    def _is_apache_model(model_name: str, terms: str) -> bool:
        normalized = f"{model_name} {terms}".lower()
        return (
            "qwen" in normalized
            or "z-image" in normalized
            or "zimage" in normalized
            or "ernie" in normalized
            or "wan" in normalized
            or "seedvr2" in normalized
            or ("flux.2-klein" in normalized and "4b" in normalized)
        )

    @staticmethod
    def _deduplicate(values: list[str]) -> list[str]:
        seen = set()
        result = []
        for value in values:
            if value in seen:
                continue
            seen.add(value)
            result.append(value)
        return result
